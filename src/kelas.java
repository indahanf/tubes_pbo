public class kelas {
    private String namaKelas;
    private int harga;

    public kelas(String namaKelas, int harga) {
        this.namaKelas = namaKelas;
        this.harga = harga;
    }

    public String getNamaKelas() {
        return namaKelas;
    }

    public int getHarga() {
        return harga;
    }
    
    
}
